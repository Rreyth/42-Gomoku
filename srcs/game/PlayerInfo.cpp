#include <game/PlayerInfo.hpp>

////////////////////////////////////////////////////////////////////////////////
// Constructors and destructor
////////////////////////////////////////////////////////////////////////////////

PlayerInfo::PlayerInfo(void)
{
	this->interType = INTER_INVALID;
	this->winState = WIN_STATE_NONE;
	this->nbCapture = 0;
	this->nbMove = 0;
}


PlayerInfo::~PlayerInfo()
{
}

////////////////////////////////////////////////////////////////////////////////
// Operators
////////////////////////////////////////////////////////////////////////////////

PlayerInfo	&PlayerInfo::operator=(const PlayerInfo &playerInfo)
{
	if (this == &playerInfo)
		return (*this);

	this->interType = playerInfo.interType;
	this->winState = playerInfo.winState;
	this->nbCapture = playerInfo.nbCapture;
	this->nbMove = playerInfo.nbMove;

	return (*this);
}

////////////////////////////////////////////////////////////////////////////////
// Public methods
////////////////////////////////////////////////////////////////////////////////

void	PlayerInfo::reset(void)
{
	this->winState = WIN_STATE_NONE;
	this->nbCapture = 0;
	this->nbMove = 0;
}


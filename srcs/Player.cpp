#include <Player.hpp>

////////////////////////////////////////////////////////////////////////////////
// Constructors and destructor
////////////////////////////////////////////////////////////////////////////////

Player::Player(void)
{
	this->type = PLAYER;
	this->captured = 0;
}

Player::~Player()
{
}

////////////////////////////////////////////////////////////////////////////////
// Getters and setters
////////////////////////////////////////////////////////////////////////////////

player_type	Player::getType(void)
{
	return (this->type);
}

void		Player::setType(player_type type)
{
	this->type = type;
}

int			Player::getCaptured(void)
{
	return (this->captured);
}

void		Player::setCaptured(int captured)
{
	this->captured = captured;
}

void		Player::setTimer(game_mode mode)
{
	if (mode == BLITZ)
	{
		this->timer = 180;
	}
	else
	{
		this->timer = 0;
	}
}

////////////////////////////////////////////////////////////////////////////////
// Public methods
////////////////////////////////////////////////////////////////////////////////

void		Player::tick(float delta, game_mode mode)
{
	if (mode == BLITZ)
	{
		this->timer -= delta;
		if (this->timer < 0)
		{
			this->timer = 0;
		}
	}
	else
	{
		this->timer += delta;
	}
}

